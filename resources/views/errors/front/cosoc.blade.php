                        <p><b>¿Qué es el COSOC?</b></p>

                        <p>Es una instancia de representación de la sociedad civil organizada, de forma territorial, funcional, sindical, gremial, entre otras. Creados a partir de la Ley Nº 20500 publicada el 16 de febrero de 2011, en reemplazo de los Consejos Económicos Sociales y Culturales Comunales (CESCOS).</p>
                        <div class="title-post">

                        </div>
                        <p><b>¿Cuáles son los aportes de la Ley Nº 20.500?</b></p>

                        <p> • Genera instancias y mecanismos de participación.</p>
                        <p>• Entrega legitimidad de la asociación y organización.</p>
                        <p>• Otorga visibilidad de la participación ciudadana.</p>
                        <p> • Busca la democratización de la gestión pública.</p>
                            <div class="title-post">

                            </div>
                        <p><b>¿Cómo se eligen los consejeros?</b></p>
                        <p>Mediante votación de los presidentes de las organizaciones, pertenecientes a cada uno de los estamentos de la sociedad civil organizada, con su vigencia al día a la fecha de la elección.</p>
                                <div class="title-post">

                                </div>
                        <p><b>¿Quién Preside y cuánto dura el Consejo?</b></p>
                        <p>Los consejeros durarán cuatro años en sus funciones. Los COSOC los preside el Alcalde, en ausencia de él, el consejo será presidido por el vicepresidente que elija el propio consejo de entre sus miembros. El secretario municipal desempeñará la función de ministro de fe de dicho organismo.</p>
                                    <div class="title-post">

                                    </div>
                        <p><b> ¿Cuál es el rol del COSOC?</b></p>
                        <p> • El Alcalde deberá informar al consejo acerca de los presupuestos de inversión, del plan de desarrollo comunal (PLADECO) y sobre las modificaciones al plan regulador, este CONSEJO dispondrá de quince días hábiles para formular sus observaciones.</p>
                        <p> • La ley no lo dice pero para opinar sobre estos aspectos se necesita que la información sea entregada en un lenguaje COMPRENSIBLE Y SENCILLO.</p>
                        <p> • En el ejercicio de su rol como consejeros, deben informar periódicamente a las organizaciones y con ellas formular las observaciones.</p>





